;
; $0680 space for the filesystem.
;

		INCBIN	"res/filesys.b09"

; FontOlde -
;
; Font data for Robert's Olde English style font.
;
; Size : $????

BANK09_PAD1::	DCB	$0680-(@-BANK09_1ST),255

		IF	VERSION_JAPAN
FontLite::	INCBIN	"res/john/font/jlite.gbf"
FontDark::	INCBIN	"res/john/font/jdark.gbf"
		ELSE
FontLite::	INCBIN	"res/john/font/lite.gbf"
FontDark::	INCBIN	"res/john/font/dark.gbf"
FontEnd::	INCBIN	"res/john/font/fontend.gbf"
		ENDC

Font09End::

; TblRotations -
;
; Tables of shifted byte patterns (MUST be on a page boundary).
;
; Size : $1000

BANK09_PAD2::	DCB	$3000-(@-BANK09_1ST),255

TblRotations::

SHIFT_COUNT	ESET	0
		REPT	8

SHIFT_MASK	ESET	$0000
		REPT	256
		DB	255&(SHIFT_MASK>>SHIFT_COUNT)
SHIFT_MASK	ESET	SHIFT_MASK+$0001
		ENDR

SHIFT_MASK	ESET	$0000
		REPT	256
		DB	255&(SHIFT_MASK>>SHIFT_COUNT)
SHIFT_MASK	ESET	SHIFT_MASK+$0100
		ENDR

SHIFT_COUNT	ESET	SHIFT_COUNT+1
		ENDR

